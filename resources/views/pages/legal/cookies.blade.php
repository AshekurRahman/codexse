                <h2 class="text-2xl font-bold text-surface-900 dark:text-white mt-12 mb-4">1. What Are Cookies?</h2>
                <p class="text-surface-600 dark:text-surface-400">Cookies are small text files that are placed on your device (computer, smartphone, or tablet) when you visit a website. They are widely used to make websites work more efficiently, provide a better user experience, and give website owners useful information about how their site is being used.</p>

                <p class="text-surface-600 dark:text-surface-400">Cookies can be "persistent" or "session" cookies:</p>
                <ul class="list-disc pl-6 text-surface-600 dark:text-surface-400 space-y-2">
                    <li><strong>Persistent cookies</strong> remain on your device after you close your browser until they expire or you delete them</li>
                    <li><strong>Session cookies</strong> are temporary and are deleted when you close your browser</li>
                </ul>

